void uart_init(uart_t uart){    
    switch (uart)
    { 
        case UART_A:
                MX_UART4_Init();
            break;
        case UART_B:
                MX_UART5_Init();
            break;
        case UART_C:
                MX_USART1_UART_Init();
            break;
        default:
            break;
    }

}



static UART_HandleTypeDef* __get_instance_uart(uart_t inst){

    UART_HandleTypeDef *uart=NULL;   
    switch (inst)   
    {
     case UART_A:
         uart= &huart4;
         break;
     case UART_B:
           uart= &huart5;
         break;
     case UART_C:
          uart= &huart1;
         break; 
    default:
        break;  

    }
 
    return uart;
}





